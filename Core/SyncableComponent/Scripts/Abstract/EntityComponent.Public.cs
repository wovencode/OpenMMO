using System;
using System.Text;
using System.Collections.Generic;
using UnityEngine;
using Mirror;
using OpenMMO;

namespace OpenMMO {
	
	// ===================================================================================
	// EntityComponent
	// ===================================================================================
	public abstract partial class EntityComponent
	{
		
		// -------------------------------------------------------------------------------
		// Warp
		// @Server
		// -------------------------------------------------------------------------------
		[ServerCallback]
		public virtual void Warp(Vector3 position)
    	{
    		agent.Warp(position);
    		RpcWarp(position);
		}
		
		// -------------------------------------------------------------------------------
		// RpcWarp
		// @Server -> @Clients
		// -------------------------------------------------------------------------------
		[ClientRpc]
   		public void RpcWarp(Vector3 position)
    	{
    		if (isLocalPlayer)
    			return;
    			
        	agent.Warp(position);
        	
    	}
		
		// -------------------------------------------------------------------------------
		
	}

}

